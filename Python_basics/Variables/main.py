# String:
name_with_single_quotes = 'python'
name_with_double_quotes = "python"
print(name_with_double_quotes)

# Concatenation two strings
string1 = "Hello"
string2 = "World"
result = string1 + " " + string2  # Adds a space between the two strings
print(result)

# Numbers(Integer):
a = 5 
b = -5
print(a)

# Numbers(float):
x = 1.3
y = -1.3
print(y)

# Booleans:
is_tall = True
is_short = False
print(is_tall)

# They can hold any value and that value can change (it varies)
acceleration = 30
acceleration = 50
print(acceleration)

# Multiple Variables with Different Values
a, b, c = 1, 2, 3
print(a, b, c)

# Multiple Variables with the Same Value
x = y = z = 10
print(x, y, z)
